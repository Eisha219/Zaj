<script>
  document.addEventListener("DOMContentLoaded", function() {
      const itemsPerPage = 8; // Number of items per page
      const items = document.querySelectorAll('.row .col'); // Selecting all items
      const totalPages = Math.ceil(items.length / itemsPerPage); // Calculate total pages
      let currentPage = 1; // Current page

      // Function to display items based on current page
      function displayItems() {
          items.forEach((item, index) => {
              const startIndex = (currentPage - 1) * itemsPerPage;
              const endIndex = startIndex + itemsPerPage;
              if (index >= startIndex && index < endIndex) {
                  item.style.display = 'block';
              } else {
                  item.style.display = 'none';
              }
          });
      }

      // Initial display
      displayItems();

      // Function to generate pagination links
      function generatePaginationLinks() {
          const paginationContainer = document.getElementById('pagination');
          paginationContainer.innerHTML = '';

          // Previous button
          const prevBtn = document.createElement('li');
          prevBtn.classList.add('page-item');
          prevBtn.innerHTML = '<a class="page-link" href="#"><</a>';
          prevBtn.addEventListener('click', () => {
              if (currentPage > 1) {
                  currentPage--;
                  displayItems();
              }
          });
          paginationContainer.appendChild(prevBtn);

          // Page numbers
          for (let i = 1; i <= totalPages; i++) {
              const pageItem = document.createElement('li');
              pageItem.classList.add('page-item');
              // if (i === currentPage) {
              //     pageItem.classList.add('active');
              // }
              pageItem.innerHTML = `<a class="page-link" href="#">${i}</a>`;
              pageItem.addEventListener('click', () => {
                  currentPage = i;
                  displayItems();
              });
              paginationContainer.appendChild(pageItem);
          }

          // Next button
          const nextBtn = document.createElement('li');
          nextBtn.classList.add('page-item');
          nextBtn.innerHTML = '<a class="page-link" href="#">></a>';
          nextBtn.addEventListener('click', () => {
              if (currentPage < totalPages) {
                  currentPage++;
                  displayItems();
              }
          });
          paginationContainer.appendChild(nextBtn);
      }

      // Generate initial pagination links
      generatePaginationLinks();
  });
</script>

